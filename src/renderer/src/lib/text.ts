/** Lowercase, strip punctuation, drop stop-words and tokens shorter than 3 chars. */
export function tokenise(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/[^a-z'\s]/g, ' ')
    .split(/\s+/)
    .filter(Boolean)
    .map((w) => w.replace(/^'+|'+$/g, ''))
    .filter((w) => w.length > 2 && !STOP_WORDS.has(w))
}
